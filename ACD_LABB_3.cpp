template<typename T>
 void List<T>::dodat() {
    int count = 0;
    Node *current = this->head;

    while (current != nullptr) {
        if (current->data > 0) {
            count++;
        }
        current = current->pNext;
    }
    push_back(count);
    cout << "Кількість доданих елементів: " << count << " була додана в кінець" << endl;
        }


template<typename T>
List<T>::List() {
    Size = 0;
    head = nullptr;
}


template<typename T>
List<T>::~List() {
    clear(); //когда наш список будет выходить из зоны действия,
    //например когда выходим из мейн, вызывается диструктор
    //и удаляем все элементы
}

template<typename T>
void List<T>::pop_back() {
    if (head == nullptr) { //список пустий
        return;
    }
    if (head->pNext == nullptr) { //один ел у списку
        delete head;
        head = nullptr;
    }
    else {
        Node *current = this->head;
        //останній елемент шукажмо
        while (current->pNext ->pNext != nullptr) { //перевіряє одного після елементу чи Є ТАМ ХТОСЬ. якщо є то цей не останнійю далі
            current = current->pNext;
        }
        delete current->pNext;
        current->pNext = nullptr; //передостанній стає останнім
    }
    Size--;
}

template<typename T>
void List<T>::pop_front() {//начинаем удаление с первого элемента
Node *temp = head;
    head = head->pNext; //делаем второй элемент новым хедом.
    //теперь у нас наш первый элемент болтаеться в воздухе под переменной темп
    delete temp;
    Size--;
}

template<typename T>
void List<T>::push_back(T data) {
    //пушбэк потому что переводиться как : затолкать назад, вставить в конец списка
    //сначала мы проверям не пустой ли у нас ХЭД. если пустой, нам надо туда что то ДОБАВИТЬ:
    if (head == nullptr) {
      head = new Node(data); //создаем новый нод и передаем ему в конструктор наши данные
        //создаем новую ноду в динамической памяти
    }
    else {
       Node *current = this -> head; //ДОБАВЛЯЕМ УКАЗАТЕЛЬ НА ХЭД во временную переменную карент
        //идем по всем нашим нодам через цикл вайл и ищем САМУЮ ПОСЛЕДНЮЮ
        while (current -> pNext != nullptr) { //проверяем на что указывает поле пайнекстр
          current = current -> pNext; //в поле карент присваиваем указатель на следующий элемент
        }
        //мы находим поле текущено элемента которое равно налпоинтеру (последний)
        current -> pNext = new Node(data);
        //ЕСЛИ У НАС ПЕРВЫЙ ЭЛЕМЕНТ, ТО УСЛОВИЕ ВАЙЛ НЕ ВЫПОЛНИТЬСЯ
        //ПОТОМУ ЧТО ПОСЛЕ ПЕРВОГО ЭЛЕМЕНТА БУДЕТ НУЛПТР
        //И ПОЭТОМУ МЫ ПРОСТО ДОБАВЛЯЕМ ДАЛЬШЕ ЭТО ГОВНО
    }
    Size++;
}

template<typename T>
void List<T>::clear() {
    while (Size) //пока сайз не будет равна 0 мы булем выполнять
        {
        pop_front(); //полностью удалим все элементы в списке.
        }
}

template<typename T>
T & List<T>::operator[](const int index) {
    //с помощью оператора квадратные скобки
    // мы ищем нужную хренб в нашем списке
    //в списке он может быть под индексом 3
    //и если нам нужны данные из списка под индексом 3
    //с помощью нашей фигни мы реализуем это дербмо и достанем данные оттуда
    int counter = 0; //заводим счетчик чтобы сравнивать его с индексами
    Node *current = this -> head; //каррент это временный обьект
    while (current != nullptr) {
        if (counter == index) { //это значит мы нашли нужный элемент и нам надо его вернуть
        return current->data;
        }
        current = current -> pNext; //у текущего элемента спрашиваем адрес следующего
        counter++;
        //будет работать до тех пор пока наша текущая каррент не булет указывать на нал
    }
}


