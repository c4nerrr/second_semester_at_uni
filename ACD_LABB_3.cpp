    //конст индекс это номер вагона Нод
    //Адрес Т потому что если без адреса то програма скопирует и отдаст тебе бумажку
    //Если написать именно адрес то функция протянет руку в вагон и вернет САМУ ПЕРЕМЕННУЮ из списка.
    //ссылка в целом чтобы иметь возможность МЕНЯТЬ а не только возвращать.
private:
   // он буквально говорит: слушай я щас напишу код но я ХЗ какой там тип данных
    //давай мы назовем неизвестный тип буквой Т и когда я буду создавать список в мейне
    //я тебе нашепчу что именно поставить под Т
    //темплате - внимание! дальше идет трафарет
